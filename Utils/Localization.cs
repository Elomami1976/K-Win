using System.Text.Json;

namespace KWin.Utils
{
    /// <summary>
    /// Static localization engine for K-win. Supports English, Arabic, French, Spanish, German, Portuguese.
    /// Uses a key-based dictionary approach with English as fallback.
    /// Language preference is persisted to AppData/K-win/language.json.
    /// </summary>
    public static class Loc
    {
        private static string _currentLang = "en";
        private static readonly Dictionary<string, Dictionary<string, string>> _translations = new();
        private static readonly string _settingsPath;

        public static string CurrentLanguage => _currentLang;
        public static bool IsRTL => _currentLang == "ar";

        /// <summary>Available languages: code ‚Üí (NativeName, EnglishName)</summary>
        public static readonly (string Code, string Native, string English)[] AvailableLanguages =
        {
            ("en", "English", "English"),
            ("fr", "Fran√ßais", "French"),
            ("es", "Espa√±ol", "Spanish"),
            ("de", "Deutsch", "German"),
            ("pt", "Portugu√™s", "Portuguese"),
            ("ar", "ÿßŸÑÿπÿ±ÿ®Ÿäÿ©", "Arabic"),
        };

        static Loc()
        {
            _settingsPath = Path.Combine(
                Environment.GetFolderPath(Environment.SpecialFolder.ApplicationData),
                "K-win", "language.json");

            RegisterEnglish();
            RegisterFrench();
            RegisterArabic();
        }

        /// <summary>Get a localized string by key. Falls back to English, then returns the key itself.</summary>
        public static string Get(string key)
        {
            if (_translations.TryGetValue(_currentLang, out var langDict) && langDict.TryGetValue(key, out var val))
                return val;
            if (_translations.TryGetValue("en", out var enDict) && enDict.TryGetValue(key, out var enVal))
                return enVal;
            return key;
        }

        /// <summary>Get a localized string with format arguments.</summary>
        public static string Get(string key, params object[] args)
        {
            var template = Get(key);
            try { return string.Format(template, args); }
            catch { return template; }
        }

        /// <summary>Set the active language and save preference.</summary>
        public static void SetLanguage(string langCode)
        {
            _currentLang = langCode;
            SavePreference();
        }

        /// <summary>Load saved language preference. Returns true if a preference was found.</summary>
        public static bool LoadPreference()
        {
            try
            {
                if (File.Exists(_settingsPath))
                {
                    var json = File.ReadAllText(_settingsPath);
                    var data = JsonSerializer.Deserialize<Dictionary<string, string>>(json);
                    if (data != null && data.TryGetValue("language", out var lang))
                    {
                        _currentLang = lang;
                        return true;
                    }
                }
            }
            catch { }
            return false;
        }

        /// <summary>Save current language to settings file.</summary>
        public static void SavePreference()
        {
            try
            {
                var dir = Path.GetDirectoryName(_settingsPath)!;
                Directory.CreateDirectory(dir);
                var json = JsonSerializer.Serialize(new Dictionary<string, string>
                {
                    ["language"] = _currentLang
                }, new JsonSerializerOptions { WriteIndented = true });
                File.WriteAllText(_settingsPath, json);
            }
            catch { }
        }

        /// <summary>Register a full language dictionary.</summary>
        public static void RegisterLanguage(string langCode, Dictionary<string, string> strings)
        {
            _translations[langCode] = strings;
        }

